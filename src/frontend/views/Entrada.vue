        Balancea para mantener un buen balance del agua, revisa frecuentemente los niveles de pH y alcalinidad. ¿Por qué? Porque los niveles de pH y alcalinidad total del agua son afectados por los productos que se usan para limpiar el agua. Los niveles de pH deben ser medidos diariamente y la alcalinidad cada mes. El rango ideal del pH debe estar entre 7.2 y 7.6 partículas por millón (ppm). Para medir la alcalinidad total, se recomienda hacerlo cuando el pH esté en niveles óptimos. La alcalinidad total del agua nos dice qué tan resistente es el agua a los cambios de pH.</p>

